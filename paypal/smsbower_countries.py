def resolve_smsbower_country_id(iso2: str | None, explicit: str | None = None) -> str:
    """Resolve SMSBower numeric country id for a protocol ISO2 code."""
    if explicit and str(explicit).strip():
        return str(explicit).strip()
    env = (os.getenv("SMSBOWER_COUNTRY") or os.getenv("PAYPAL_SMSBOWER_COUNTRY") or "").strip()
    if env.isdigit() or (env and env.lstrip("-").isdigit()):
        return env
    # optional JSON override map
    raw_map = (os.getenv("SMSBOWER_COUNTRY_MAP_JSON") or "").strip()
    if raw_map:
        try:
            data = json.loads(raw_map)
            if isinstance(data, dict):
                code = (iso2 or "").upper()
                if code in data:
                    return str(data[code])
        except Exception:
            pass
    code = (iso2 or "BR").upper()
    if code == "UK":
        code = "GB"
    return SMSBOWER_COUNTRY_IDS.get(code, SMSBOWER_COUNTRY_IDS.get("BR", "73"))


def list_mapped_iso_codes() -> list[str]:
    return sorted(set(SMSBOWER_COUNTRY_IDS.keys()))
